# colors from https://ss64.com/nt/syntax-ansi.html

class Colors:
    DEFAULT = '\033[0m'
    BLACK = '\033[30m'
    DARK_RED = '\033[31m'
    DARK_GREEN = '\033[32m'
    DARK_YELLOW = '\033[33m'
    DARK_BLUE = '\033[34m'
    DARK_MAGENTA = '\033[35m'
    DARK_CYAN = '\033[36m'
    DARK_WHITE = '\033[37m'
    BRIGHT_BLACK = '\033[90m'
    BRIGHT_RED = '\033[91m'
    BRIGHT_GREEN = '\033[92m'
    BRIGHT_YELLOW = '\033[93m'
    BRIGHT_BLUE = '\033[94m'
    BRIGHT_MAGENTA = '\033[95m'
    BRIGHT_CYAN = '\033[96m'
    WHITE = '\033[97m'
    BLACK_BACKGROUND = '\033[40m'
    DARK_RED_BACKGROUND = '\033[41m'
    DARK_GREEN_BACKGROUND = '\033[42m'
    DARK_YELLOW_BACKGROUND = '\033[43m'
    DARK_BLUE_BACKGROUND = '\033[44m'
    DARK_MAGENTA_BACKGROUND = '\033[45m'
    DARK_CYAN_BACKGROUND = '\033[46m'
    DARK_WHITE_BACKGROUND = '\033[47m'
    BRIGHT_BLACK_BACKGROUND = '\033[100m'
    BRIGHT_RED_BACKGROUND = '\033[101m'
    BRIGHT_GREEN_BACKGROUND = '\033[102m'
    BRIGHT_YELLOW_BACKGROUND = '\033[103m'
    BRIGHT_BLUE_BACKGROUND = '\033[104m'
    BRIGHT_MAGENTA_BACKGROUND = '\033[105m'
    BRIGHT_CYAN_BACKGROUND = '\033[106m'
    WHITE_BACKGROUND = '\033[107m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'


def color_text(text, color):
    return color + text + Colors.DEFAULT
